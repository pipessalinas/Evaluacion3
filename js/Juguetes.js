class Juguetes {
    constructor(id, nombre, edadRecomendada, pais, modelo, material, categoria, cantidadEnStock, precio, marca, estado, tipo){
        this.id = id;
        this.nombre = nombre;
        this.edadRecomendada = edadRecomendada;
        this.pais = pais;
        this.modelo = modelo;
        this.material = material;
        this.categoria = categoria;
        this.cantidadEnStock = cantidadEnStock;
        this.precio = precio;
        this.marca = marca;
        this.estado = estado;
        this.tipo = tipo;
    }
}
